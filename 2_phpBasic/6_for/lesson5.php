<?php
// 以下をfor文を使用して表示してください。
// ヒント: forの中でforを３回使用

// ********1
// *******121
// ******12321
// *****1234321
// ****123454321
// ***12345654321
// **1234567654321
// *123456787654321
// 12345678987654321

$term2 = '';
for($i = 1; $i <= 9; $i++){
  $term1 = str_repeat("*", 9 - $i);
  $term2 = $term2.$i;
  $term3 = strrev(substr($term2, 0, -1));

  echo $term1.$term2.$term3;
  echo '<br/>';
};

?>